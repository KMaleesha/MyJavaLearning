public class ifElse {
    public static void main(String[] args){
        int x = 50;
        if (x == 10){
            System.out.println("equal");
        }else if(x > 10){
            System.out.println("yes");
        }else if (x< 10){
            System.out.println("no");
        }else if (x != 10) {
            System.out.println("not equal");
        }else{
            System.out.println("none");
        }
    }
}
