package accessModifiers.publicAndPrivate.pack1;

public class Maleesha {
    private int x = 10;
    public int y = 20;
    protected int z = 30;

    public static void main(String args[]){
        Maleesha maleesha = new Maleesha();
        System.out.println(maleesha.x);
        System.out.println(maleesha.x);
    }
}
