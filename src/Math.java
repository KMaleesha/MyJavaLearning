public class Math {
    public static void main(String[] args){

        int x, y;
        x = 10;
        y = 34;

        // addition
        int addition = x+y;
        System.out.println(addition);

        // division
        float division = y/x;
        System.out.println(division);

        // multiplication
        int multi = x*y;
        System.out.println(multi);

        //modules
        int modules = y%x;
        System.out.println(modules);

        // subtraction
        int subtraction = y-x;
        System.out.println(subtraction);
    }
}
