public class Loop  {
    public static void main(String[] args){
        int i = 0;
        while (i<=20){
            System.out.println(i);
            i++;
        }
        System.out.println("-----------------");

        int x = 0;
        do{
            System.out.println("Hi Maleesha");
            x++;
        }while(x<=10);
        System.out.println("-----------------");

        for(int a = 0; a<=10; a++){
            System.out.println("Hi For Loop");
        }
    }
}
